package herbalbee.objetos;

public class Usuario {
	private int id;
	private String sUsu;
	private String sNombre;
	private String sApellido;
	private String sRun;
	private String sEmail;
	private String sPassword;
	private int sTelefono;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getsUsu() {
		return sUsu;
	}
	public void setsUsu(String sUsu) {
		this.sUsu = sUsu;
	}
	public String getsNombre() {
		return sNombre;
	}
	public void setsNombre(String sNombre) {
		this.sNombre = sNombre;
	}
	public String getsApellido() {
		return sApellido;
	}
	public void setsApellido(String sApellido) {
		this.sApellido = sApellido;
	}
	public String getsRun() {
		return sRun;
	}
	public void setsRun(String sRun) {
		this.sRun = sRun;
	}
	public String getsEmail() {
		return sEmail;
	}
	public void setsEmail(String sEmail) {
		this.sEmail = sEmail;
	}
	public String getsPassword() {
		return sPassword;
	}
	public void setsPassword(String sPassword) {
		this.sPassword = sPassword;
	}
	public int getsTelefono() {
		return sTelefono;
	}
	public void setsTelefono(int sTelefono) {
		this.sTelefono = sTelefono;
	}	

}
